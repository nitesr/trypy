num1 = 10
num2 = 30

# addition
add = num1 + num2
print(num1, " + ", num2, " = ", add)

# subtraction
sub = num2 - num1
print(num2, " - ", num1, " = ", sub)

# multiplication
mul = num1 * num2
print(num1, " * ", num2, " = ", mul)

# decimal division
div = num1 / num2
print(num1, " / ", num2, " = ", div)

# integer division
intdiv = num1 // num2
print(num1, " // ", num2, " = ", intdiv)

# power
pow = num1 ** 2
print(num1, " ** ", 2, " = ", pow)

# modulus
mod = num2 % num1
print(num2, " % ", num1, " = ", mod)
