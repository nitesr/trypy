# string
name = "your-name"

# integer
age = 10

# boolean
is_male = True

# decimal
cgpa = 3.7

# print
print("name = ", name)
print("age = ", age)
print("is_male = ", is_male)
print("cgpa = ", cgpa)

# print datatype of the variable
print("type(age) is ", type(age))

# we can change the type of the variable
print("age type is changed from int to string")
age = "my age is 10"
print("age = ", age)
print("type(age) is ", type(age))
